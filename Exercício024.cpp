#include <iostream>

int main()
{
   
	for (int i = 15; i <= 20; i++) // Função "FOR" de repetição (Declaração da variável "i" como inteira e determinando o inicio do intervalo; limite do intervalo; contar de 1 em 1)
	{
		printf("%d \n", i); // Imprimir na tela a variavel inteira, variando no intervalo crescente de [15;20]
	}

}


