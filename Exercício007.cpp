#include <iostream>

int main()
{
	float altura = 1.20;  /* Declaração das Variáveis */
	float raio = 1;    /* Declaração das Variáveis */
	float volume;  /* Declaração das Variáveis */
	 
	
	volume = 3.14159 * raio * raio * altura; // Atribuindo valores as variáveis 

	printf("Volume da lata de azeite....: %.2f \n", volume);  // Imprimir na tela o resultado do volume 

	system("pause"); 
}


